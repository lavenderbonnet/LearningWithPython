n = int(input("Choose a number 0 - 6 "))

if n == 0:
    print("It's Sunday!")

elif n == 1:
    print("It's Monday!")

elif n == 2:
    print("It's Tuesday!")

elif n == 3:
    print("It's Wednesday!")

elif n == 4:
    print("It's Thursday!")

elif n == 5:
    print("It's Friday!")

elif n == 6:
    print("It's Saturday!")

else:
    print("I said 0 - 6.")
